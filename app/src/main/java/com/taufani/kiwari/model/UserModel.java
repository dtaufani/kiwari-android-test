public class UserModel {
    String avatar;
    String name;
    String email;
    String password;

    public UserModel() {
    }

    public String getAvatar() {
        return avatar;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }
}
